    input_df = pd.DataFrame([input_data])

    # load the trained model (pickled)
    with open('best_model.pkl', 'rb') as file:
        model = pickle.load(file)

    # prediction button
    if st.button("Predict Booking Status"):
        prediction = model.predict(input_df)[0]
        probability = model.predict_proba(input_df)[0][1]  # Probability of cancellation (class 1)

        if prediction == 1:
            st.error(f"‚ùå Booking is likely to be canceled with a probability of {probability:.2%}.")
        else:
            st.success(f"‚úÖ Booking is likely to be confirmed (Not Canceled) with a probability of {(1 - probability):.2%}.")

